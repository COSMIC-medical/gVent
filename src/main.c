/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  HAL_StatusTypeDef ret;
  uint8_t buf[20];
  uint32_t val;
  // uint32_t flow_slpm;
  uint32_t pressure_cmh2o; 
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  MX_TIM3_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  buf[0] = FS6122_READ_ADDRESS;
  ret = HAL_I2C_Master_Transmit(&hi2c1, SENSOR2_ADDRESS, buf, 1, HAL_MAX_DELAY);
  if ( ret != HAL_OK ) {
      strcpy((char*)buf, "Error Tx\r\n");
    } 
  HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    // Tell FS6122 that we want to read from the pressure register
    buf[0] = FS6122_READ_FLOWRATE_PRESSURE;
    ret = HAL_I2C_Master_Transmit(&hi2c1, SENSOR2_ADDRESS, buf, 1, HAL_MAX_DELAY);
    if ( ret != HAL_OK ) {
      strcpy((char*)buf, "Error Tx\r\n");
    } 
    else {

      // Read 4 bytes from the pressure register
      ret = HAL_I2C_Master_Receive(&hi2c1, SENSOR2_ADDRESS, buf, 4, HAL_MAX_DELAY);
      if ( ret != HAL_OK ) {
        strcpy((char*)buf, "Error Rx\r\n");
      } 
      else {

        //Combine the bytes
        val = ((int16_t)buf[0] << 24) | (buf[1] << 16) | (buf[2] << 8) | (buf[3]) >> 2;
        // val >>= 2; // value is 14 bit, so shift by 2

        // Convert to 2's complement, since temperature can be negative
        // if ( val > 0x7FF ) {
        //   val |= 0xF000;
        // }

        // Convert to float temperature value (Celsius)
        pressure_cmh2o = val;

        // Convert temperature to decimal format
        // pressure_cmh2o *= 100;
        sprintf((char*)buf,
              "%li cmH2O\r\n",
              (pressure_cmh2o)
              );
      }
    }

    // Send out buffer (temperature or error message)
    HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);

    // Wait
    HAL_Delay(500);

    // INIT STEP 3: run the platform
    // run_platform();
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // we should never reach this state, but if we
	  // do something went wrong; call the DSS.
	  // dss();
  }
  /* USER CODE END 3 */
}

